/**
 * @author Siegfried GENG
 * @date 2019/8/5 - 15:54
 */
@Target({TYPE, FIELD, METHOD, PARAMETER, CONSTRUCTOR, LOCAL_VARIABLE})
@Retention(RetentionPolicy.RUNTIME)
public @interface MyAnnotation {

    String value() default "gengyu";

}
