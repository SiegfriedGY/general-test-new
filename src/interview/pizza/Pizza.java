package interview.pizza;

/**
 * Add to this class or write new classes so that a customer can order and
 * custom make his own pizza.
 *
 * @author haksunli
 */
public class Pizza {

    private String dough;
    private String sauce;
    private String topping;

    // TODO: add code
    public Pizza() {
        // default settings
        this.dough = "basic_dough";
        this.sauce = "basic_sauce";
        this.topping = "basic_topping";
    }

    public String getDough() {
        return dough;
    }

    public void setDough(String dough) {
        this.dough = dough;
    }

    public String getSauce() {
        return sauce;
    }

    public void setSauce(String sauce) {
        this.sauce = sauce;
    }

    public String getTopping() {
        return topping;
    }

    public void setTopping(String topping) {
        this.topping = topping;
    }

    @Override
    public String toString() {
        return "Dough:" + dough + " Topping:" + topping + " Sauce:" + sauce;
    }

}
